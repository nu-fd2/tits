#include "../../includes/Token_and_lex.h"


void	fre_chr_env(char **env)
{
	char	**ts;
	int	i;

	i = 0;
	ts = env;
	while(ts[i])
	{
		free(ts[i]);
		i++;
	}
	free(env);
}
